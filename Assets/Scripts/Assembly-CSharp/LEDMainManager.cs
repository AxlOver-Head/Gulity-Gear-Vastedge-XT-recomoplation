	public void start(LINE _line, int _id, bool _loop)
	{
		if (_line >= LINE.STATE && _line < LINE.MAX && _id >= 0 && _id < 830)
		{
			ledLines[(int)_line].start(_id, filename[_id], _loop);
		}
	}

	public void kstart(LINE _line, int _id, bool _loop)
	{
		if (_line >= LINE.STATE && _line < LINE.MAX && _id >= 0 && _id < 830)
		{
			ledLines[(int)_line].kstart(_id, filename[_id], _loop);
		}
	}

	public void stop(LINE _line)
	{
		if (_line >= LINE.STATE && _line < LINE.MAX)
		{
			ledLines[(int)_line].stop();
		}
	}

	public void stop()
	{
		for (int i = 0; i < 17; i++)
		{
			stop((LINE)i);
		}
	}

	private void blendLines()
	{
		Array.Clear(blendData, 0, LEDMainLine.DATA_NUM);
		if (ledLines[16].m_Active)
		{
			ledLines[16].blendRewriteAll(blendData);
			return;
		}
		for (int i = 0; i < 16; i++)
		{
			if (ledLines[i].m_Active)
			{
				if (i == 0)
				{
					ledLines[i].blendRewriteAll(blendData);
				}
				else
				{
					ledLines[i].blendRewrite(blendData, blendFlag, i);
				}
			}
		}
	}

